public class loopings {
    public static void main(String[] args) {

        //while loop
        int num=1;
        while(num<=3){
            System.out.println(num);
            num++;
        }

        //do while loop
        int a=0;
        do{
            System.out.println(a);
            a++;
        }
        while(a<5);

        //for loop
        for(int i=0;i<=2;i++){
            System.out.println("hello world");
        }


        
    }
}
